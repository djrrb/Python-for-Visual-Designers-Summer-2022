for myNum in range(20):
    # the number (5)
    # normal divide (5 / 2 = 2.5)
    # whole number divide (5 // 2 = 2 goes into 5 two whole times)
    # modulo: whole number remainder (5 % 2 = 1, 2 goes into 5 two whole times and there's one left over)
    print(myNum, myNum / 2, myNum // 2, myNum % 2)